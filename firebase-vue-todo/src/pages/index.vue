<template>
  <div>
    <h1>Todoリスト</h1>
    <todo-form @handleParentAddTodo="handleParentAddTodo"/>
    <todo-List
      :todos="todos"
      @handleParentDeleteTodo = "handleParentDeleteTodo"
      @handleParentCompleteTodo = "handleParentComplateTodo" />
  </div>
</template>

